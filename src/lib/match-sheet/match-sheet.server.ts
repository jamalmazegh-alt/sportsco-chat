/**
 * Match Sheet / Player List PDF generation (V1).
 *
 * Architecture rules (see brief):
 *  - Server-side only. The caller MUST be authorized as a coach/admin of the
 *    event's team via the existing `is_team_coach` RPC. We never trust the UI.
 *  - PII surface is intentionally minimal: jersey, first/last name, birth date,
 *    license number — and only for convocated players of THIS event's team.
 *  - Every successful generation is written to `audit_logs`.
 *  - Service-role admin client is used for the read because RLS already restricts
 *    what `is_team_coach` allows; we never widen the column set beyond what is
 *    on the brief.
 *  - SVG club logos are rejected (no raw embed — XSS hardening parity with the
 *    public roster logo upload).
 */
